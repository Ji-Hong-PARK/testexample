package kr.ac.kopo.foodmall.model;

public class Member {
	private String memberId;
	private String memberPassword;
	private String memberName;
	private String memberMail;
	private int memberTel;
	private String memberAddress;
	private int memberMileage;
	
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getMemberPassword() {
		return memberPassword;
	}
	public void setMemberPassword(String memberPassword) {
		this.memberPassword = memberPassword;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getMemberMail() {
		return memberMail;
	}
	public void setMemberMail(String memberMail) {
		this.memberMail = memberMail;
	}
	public int getMemberTel() {
		return memberTel;
	}
	public void setMemberTel(int memberTel) {
		this.memberTel = memberTel;
	}
	public String getMemberAddress() {
		return memberAddress;
	}
	public void setMemberAddress(String memberAddress) {
		this.memberAddress = memberAddress;
	}
	public int getMemberMileage() {
		return memberMileage;
	}
	public void setMemberMileage(int memberMileage) {
		this.memberMileage = memberMileage;
	}
	
	
}
